// Subtração

let a = -2;
let b = 10;

let subtracao = a - b;
console.log("O resultado da subtração é: " + subtracao);

// Multiplicação

let c = 8;
let d = 6;

let multiplicacao = (c * d) + (d - c);
console.log("O resultado da multiplicação é: " + multiplicacao);

// Divisão

let e = 4;
let f = 50;

let divisao = f / e;
console.log("O resultado da divisão é: " + divisao);

// Mais operações

let comprovante = subtracao + multiplicacao + divisao;
console.log("O comprovante de todas as somas é: " + comprovante);

let comprovante2 = (divisao * subtracao) + multiplicacao;
console.log("O resultado de comprovante2: " + comprovante2);

 // Contador

 let contador = 10;
 contador++;
 console.log("O incremento final é: " + contador);


